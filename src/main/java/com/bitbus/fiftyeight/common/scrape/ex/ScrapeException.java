package com.bitbus.fiftyeight.common.scrape.ex;

@SuppressWarnings("serial")
public class ScrapeException extends Exception {

    public ScrapeException() {
        super();
    }

    public ScrapeException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public ScrapeException(String arg0) {
        super(arg0);
    }

    public ScrapeException(Throwable arg0) {
        super(arg0);
    }


}
