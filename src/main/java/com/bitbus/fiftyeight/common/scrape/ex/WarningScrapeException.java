package com.bitbus.fiftyeight.common.scrape.ex;

@SuppressWarnings("serial")
public class WarningScrapeException extends ScrapeException {

    public WarningScrapeException() {
        super();
    }

    public WarningScrapeException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public WarningScrapeException(String arg0) {
        super(arg0);
    }

    public WarningScrapeException(Throwable arg0) {
        super(arg0);
    }


}
